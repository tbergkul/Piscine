int	ft_strcmp(char *s1, char *s2)
{
	int x;
	int y;
	int z;

	x = 0;
	y = 0;
	z = 0;
	while (s1[x] != '\0')
	{
		y = y + s1[x];
		x++;
	}
	x = 0;
	while (s2[x] != '\0')
	{
		z = z + s2[x];
		x++;
	}
	x = y - z;
	return (x);
}
